// 4. Запросите у пользователя радиус окружности и выведите площадь такой окружности.
let r = prompt('Сообщите радиус окружности, м.');
let s = Math.PI * Math.pow(r, 2);
alert('Площадь окружности равна ' + s.toFixed(1) + ' м2.');

// 5. Запросите у пользователя расстояние в км между двумя городами и за сколько часов он хочет добраться. Посчитайте скорость, с которой необходимо двигаться, чтобы успеть вовремя.
let l = prompt('Укажите расстояние в км между двумя городами.');
let t = prompt('за сколько часов вы хотите добраться?');
let v = l / t;
alert('Скорость, с которой необходимо двигаться, чтобы успеть вовремя ' + v.toFixed() + ' км/ч.');

// 6. Реализуйте конвертор валют. Пользователь вводит доллары, программа переводит в евро. Курс валюты храните в константе.
let dollars = prompt('Введите количество долларов.');
const DOLLAREURO = 1.10;
