// 7. Пользователь указывает объем флешки в Гб. Программа должна посчитать, сколько файлов размером в 820 Мб помещается на флешку.
let flesh = prompt('Укажите объём флешки в Гб.');
const FILE = 820;
const MBINGB = 1000;
let files = Math.floor(flesh * MBINGB / FILE);
alert(files + ' файлов размером в 820 Мб помещается на флешку.');

// 8. Пользователь вводит сумму денег в кошельке и цену одной шоколадки. Программа выводит, сколько шоколадок может купить пользователь, и сколько сдачи у него останется.
let money = prompt('Какая сумма денег в вашем кошельке?');
let price = prompt('Укажите цену одной шоколадки.');
let chocolatebar = Math.floor(money / price);
let shortchange = money - chocolatebar * price;
alert('Вы можете купить ' + chocolatebar + ' шоколадок и у вас останется ' + shortchange.toFixed(2) + ' грн. сдачи.');

// 9. Запросите у пользователя трехзначное число и выведите его задом наперед. Для решения задачи вам понадобится оператор % (остаток от деления).
let threedigit = prompt('Введите трехзначное число.');
let one = Math.floor(threedigit / 100);
let two = Math.floor(threedigit % 100 / 10);
let three = threedigit % 100 % 10;
alert('Ваше число задом наперёд ' + three + two + one);

// 10. Пользователь вводит сумму вклада в банк на 2 месяца, с процентной ставкой депозита 5% годовых. Вывести сумму начисленных процентов.
let deposit = prompt('Введите сумму вклада.');
const term = 2;
const percent = 0.05;
let interest1 = term / 12 * percent * deposit;
let interest2 = deposit * percent / 12 + deposit * (1 + percent / 12) * percent / 12;
alert('Сумма начисленных процентов за 2 месяца ' + interest1.toFixed(2) + ' грн. Сумма начисленных процентов за 2 месяца при ежемесячной капитализации ' + interest2.toFixed(2) + ' грн.');